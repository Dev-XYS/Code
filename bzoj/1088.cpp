#include <cstdio>

using namespace std;

int n, d[10010];
long long dp[10010][2][2][2];

int main()
{
	scanf("%d", &n);
	for (int i = 1; i <= n; i++)
	{
		scanf("%d", &d[i]);
	}
	if (d[1] == 0)
	{
		dp[1][0][0][0] = 1;
	}
	else if (d[1] == 1)
	{
		dp[1][0][0][1] = dp[1][0][1][0] = 1;
	}
	else
	{
		dp[1][0][1][1] = 1;
	}
	for (int i = 2; i <= n; i++)
	{
		if (d[i] == 0)
		{
			dp[i][0][0][0] = dp[i - 1][0][0][0] + dp[i - 1][1][0][0];
		}
		else if (d[i] == 1)
		{
			dp[i][1][0][0] = dp[i - 1][0][1][0] + dp[i - 1][1][1][0];
			dp[i][0][1][0] = dp[i - 1][0][0][1] + dp[i - 1][1][0][1];
			dp[i][0][0][1] = dp[i - 1][0][0][0] + dp[i - 1][1][0][0];
		}
		else if (d[i] == 2)
		{
			
			dp[i][1][1][0] = dp[i - 1][0][1][1] + dp[i - 1][1][1][1];
			dp[i][0][1][1] = dp[i - 1][0][0][1] + dp[i - 1][1][0][1];
			dp[i][1][0][1] = dp[i - 1][0][1][0] + dp[i - 1][1][1][0];
		}
		else
		{
			dp[i][1][1][1] = dp[i - 1][0][1][1] + dp[i - 1][1][1][1];
		}
	}
	printf("%lld", dp[n][0][0][0] + dp[n][0][1][0] + dp[n][1][0][0] + dp[n][1][1][0]);
	return 0;
}
